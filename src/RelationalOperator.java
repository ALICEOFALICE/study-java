public class RelationalOperator {
    public static void main(String[] args) {
        System.out.println(1 >= 2);//计算1是否大于等于2
        System.out.println(2 >= 2);//计算2是否大于等于2
        System.out.println(3 >= 2);//计算3是否大于等于2
        //=========================================//
        System.out.println(1 <= 2);//计算1是否小于等于2
        System.out.println(2 <= 2);//计算2是否小于等于2
        System.out.println(3 <= 2);//计算3是否小于等于2
        //=========================================//
        System.out.println(2 == 2);//计算2是否等于2
        System.out.println(2 != 2);//计算2是否不等于2

    }
}
